/**     Operador typeof
 * 
 *  Quando o tipo da variável ou expressão for desconhecida, 'typeof' pode ser ulitizado para determinar/descobrir o tipo
 *  VEJA O EXEMPLO
 */
let variavel1 = "Olá, mundo!";
let variavel2 = 42;

console.log(typeof variavel1); // Output: string
console.log(typeof variavel2); // Output: number